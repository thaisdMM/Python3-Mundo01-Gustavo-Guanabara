# quanto dinheiro a pessoa tem na carteira e quantos dolares ela pode comprar - dolar na aula 3,27

dinheiro_carteira = float(input("Informe quanto dinheiro você tem na sua carteira R$: "))
print(f"A conversão de {dinheiro_carteira:.2f} reais para dólares com a cotação de 3,27 é: {dinheiro_carteira / 3.27:.2f} US$ ")